    public void CalcularPromedio(int numero, String tipo){
        int tiempo = this.data_datos.getRowCount();
        //JOptionPane.showMessageDialog(rootPane, tiempo);
        double evento = 0, valor = 0, promedio = 0, diferencia = 0;
        //String mensaje = "";
        if(numero <= (tiempo-1)){
            for (int i = 0; i <= tiempo-1; i++)
			{
			this.data_datos.setValueAt("", i, 2);
                        this.data_datos.setValueAt("", i, 3);
                        //this.data_datos.setValueAt("", i, 4);
			}
            for (int i = numero; i <tiempo; i++){
				for (int j = 1; j <numero+1; j++)
				{
                                    String dato1 = (String) (this.data_datos.getValueAt(i-j, 1));
                                    
                                    dato1=dato1.replaceAll(",", ".");
                                    evento += Double.parseDouble(dato1);
					//valor += Convert::ToDouble(this->dataGridView1->Rows[i - j]->Cells[1]->Value);
				}
				promedio = evento / numero;
				if (this.data_datos.getValueAt(i, 1) != "")
				{
                                    String dato1 = (String) (this.data_datos.getValueAt(i, 1));
                                    dato1 = dato1.replaceAll(",", ".");
                                    valor = Double.parseDouble(dato1);
					// valor = Convert::ToDouble(this->dataGridView1->Rows[i]->Cells[1]->Value);
				}
				
				diferencia = valor - promedio;
                                DecimalFormat decimales = new DecimalFormat("0.00");  
                                this.data_datos.setValueAt(""+decimales.format(promedio)+"", i, 2);
                                this.data_datos.setValueAt(""+decimales.format(diferencia)+"", i, 3);
                                
				evento = 0;
				valor = 0;
			}
			//Graficar();
                                 //para generar primera gráfica
                         double [] x1 = new double[tiempo-1];
                         double [] y1 = new double[tiempo-1];
                         for (int i = 0; i <tiempo-1; i++){
                           x1[i] = i+1;
                           String datoy = (String) (this.data_datos.getValueAt(i, 1));  
                           datoy = datoy.replaceAll(",", ".");
                           y1[i] = Double.parseDouble(datoy);
                         }
                            //dibujar primera
                            grafica.setVisible(true);
                            grafica.addLegend("SOUTH");
                            grafica.removeAllPlots();
                            grafica.addLinePlot("Valores reales",Color.GREEN, x1,y1);
                              //generar segunda grafica
                             double [] x2 = new double[tiempo-numero];
                             double [] y2 = new double[tiempo-numero];
                             int z = tiempo-numero;
                             int c = tiempo-z;
                               for(int j=0;j<z;j++){
                                 x2[j] = c+(j+1) ;
                                 String datoy2 = (String) (this.data_datos.getValueAt(c+j, 2));
                                 datoy2 = datoy2.replaceAll(",", ".");
                                 y2[j] = Double.parseDouble(datoy2);
                               }
                                 //dibujar grafica
                                  grafica.addLinePlot("Promedio",Color.RED, x2,y2);        
            
		} else{
            JOptionPane.showMessageDialog(rootPane, "Para calcular el promedio " + tipo + " se deben generar mínimo " + numero + " eventos");
            limpiar();
        }
        
        
        
    }
    
