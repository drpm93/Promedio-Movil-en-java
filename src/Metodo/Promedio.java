    //funcion para limpiar elementos
    public void limpiar(){
        txt_eventos.setText("");
        limpiargrilla();
        cmd_generar.setEnabled(true);
        grafica.setVisible(false);
        txt_eventos.requestFocus();
        cmb_opciones.setSelectedItem("Bimensual");
        
    }
    
    public void limpiargrilla(){
        DefaultTableModel tabla;
            tabla = (DefaultTableModel) this.data_datos.getModel();
            int a =tabla.getRowCount();
            for(int i=0; i<a; i++)
                tabla.removeRow(0);
    }
    
    
    private void cmd_generarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_generarActionPerformed
        // TODO add your handling code here:
        
        int x = Integer.parseInt(txt_eventos.getText());
        if (x <= 0){
            JOptionPane.showMessageDialog(txt_eventos, "El número debe ser mayor a 0");
        } else{
           if(this.data_datos.getRowCount() > 0){
               this.data_datos.setModel(new DefaultTableModel());
           }
        }
      for(int i=1;i<x+2;i++){
             if(i==(x+1)){
                 ((DefaultTableModel) data_datos.getModel()).addRow(new Object[]{"","0"});
                 
                 
             } else{
                 
                 ((DefaultTableModel) data_datos.getModel()).addRow(new Object[]{i});
             }
      }
           cmd_generar.setEnabled(false);
        
    }//GEN-LAST:event_cmd_generarActionPerformed

